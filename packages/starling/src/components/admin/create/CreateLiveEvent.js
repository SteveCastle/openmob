import React from 'react'
import PropTypes from 'prop-types'
import { useMutation } from 'react-apollo-hooks'
import gql from 'graphql-tag'
import { Formik } from 'formik'
import Content from '@openmob/bluebird/src/components/layout/Content'
import Card from '@openmob/bluebird/src/components/cards/Card'
import Form from '@openmob/bluebird/src/components/forms/Form'
import Widget from '@openmob/bluebird/src/components/forms/Widget'
import Label from '@openmob/bluebird/src/components/forms/Label'
import Input from '@openmob/bluebird/src/components/forms/Input'
import TextArea from '@openmob/bluebird/src/components/forms/TextArea'
import Button from '@openmob/bluebird/src/components/buttons/Button'

const CREATE_LIVEEVENT = gql`
  mutation createLiveEvent($liveEvent: LiveEventInput) {
    createLiveEvent(liveEvent: $liveEvent, buildStatic: true) {
      ID
    }
  }
`

const CreateLiveEvent = ({ id }) => {
  const createLiveEvent = useMutation(CREATE_LIVEEVENT)

  return (
    <Formik
      onSubmit={(values, { setSubmitting }) =>
        createLiveEvent({
          variables: {
            liveEvent: {
              ...values,
            },
          },
        })
      }
    >
      {props => {
        const { values, handleChange, handleBlur, handleSubmit } = props
        return (
          <Content>
            <Card>
              <Form>
                <h1>Create LiveEvent</h1>
                <Widget>
                  <Label>Title</Label>
                  <Input
                    value={values.Title}
                    type="text"
                    name="Title"
                    onChange={handleChange}
                    onBlur={handleBlur}
                  />
                </Widget>
                <Widget>
                  <Label>LiveEventType</Label>
                  <Input
                    value={values.LiveEventType}
                    type="text"
                    name="LiveEventType"
                    onChange={handleChange}
                    onBlur={handleBlur}
                  />
                </Widget>

                <Button
                  label="Save"
                  block
                  variant="primary"
                  onClick={handleSubmit}
                />
              </Form>
            </Card>
          </Content>
        )
      }}
    </Formik>
  )
}

CreateLiveEvent.propTypes = {
  id: PropTypes.string,
}

export default CreateLiveEvent
